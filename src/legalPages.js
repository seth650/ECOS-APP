/** Map pathname → app phase for /terms and /privacy. */
export function phaseFromPathname(pathname = "") {
  const p = String(pathname || "").replace(/\/+$/, "") || "/";
  if (p === "/terms") return "terms";
  if (p === "/privacy") return "privacy";
  return null;
}

export function pathForLegalPhase(phase) {
  if (phase === "terms") return "/terms";
  if (phase === "privacy") return "/privacy";
  return "/";
}
